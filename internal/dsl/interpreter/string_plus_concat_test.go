package interpreter_test

import (
	"testing"
)

// Issue #316: оператор «+» над двумя строками должен ВСЕГДА конкатенировать,
// даже если обе строки состоят из одних цифр. Раньше числовые строки
// приводились к числам и складывались ("0" + "7" → 7), из-за чего ломалась
// доливка ведущих нулей и сборка сортируемых ключей из цифровых фрагментов.

func TestConcat_TwoDigitStrings(t *testing.T) {
	src := `Функция Тест()
  Возврат "0" + "7";
КонецФункции`
	got := evalFunc(t, src)
	if got != "07" {
		t.Errorf(`"0" + "7" = %v (%T), ожидалось "07"`, got, got)
	}
}

func TestConcat_StringPlusStringConversion(t *testing.T) {
	src := `Функция Тест()
  Возврат "0" + Строка(7);
КонецФункции`
	got := evalFunc(t, src)
	if got != "07" {
		t.Errorf(`"0" + Строка(7) = %v (%T), ожидалось "07"`, got, got)
	}
}

// Практический кейс из ишью: сборка сортируемого ключа месяца с ведущим нулём.
func TestConcat_LeadingZeroMonthKey(t *testing.T) {
	src := `Функция Тест()
  Месяц = 7;
  Возврат "2026-0" + Строка(Месяц);
КонецФункции`
	got := evalFunc(t, src)
	if got != "2026-07" {
		t.Errorf(`ключ месяца = %v, ожидалось "2026-07"`, got)
	}
}

// Защита от регрессии: настоящие числа (в т.ч. decimal, каким приходят поля БД)
// по-прежнему складываются, а не конкатенируются — гейт строка+строка не должен
// перехватывать числовые операнды. 1/2 даёт decimal.Decimal, не строку.
func TestConcat_DecimalPlusNumber_StillNumeric(t *testing.T) {
	src := `Функция Тест()
  Возврат (1 / 2) + 2;
КонецФункции`
	got := evalFunc(t, src)
	if !numEq(got, 2.5) {
		t.Errorf("(1/2) + 2 = %v (%T), ожидалось 2.5", got, got)
	}
}
