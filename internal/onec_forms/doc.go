// Package onec_forms — двусторонний конвертер между управляемыми формами
// OneBase (.form.yaml + .form.os + _resources/) и форматом управляемых форм
// внешней Enterprise-системы (Form.xml + Module.bsl + Items/*).
//
// Реализован через нейтральное промежуточное представление IR
// (см. ir.go): один Form.xml ↔ IRForm ↔ один .form.yaml. Это даёт чистый
// round-trip без перекрёстных зависимостей.
//
// Пакет изолирован от рантайма OneBase: зависит только от
// internal/metadata (типы форм для конечного маппинга), encoding/xml,
// gopkg.in/yaml.v3 и стандартной библиотеки.
//
// Юридический контекст: парсер работает с публичными текстовыми форматами
// данных (XML, BSL), маппинги (типы/события/элементы) реализованы как
// собственные таблицы соответствий по общедоступной документации.
// Подробности см. в docs/forms-1c-converter.md.
//
// Foundation-фаза (этап 1 плана 37): только структуры IR, diagnostics
// и пустые таблицы маппингов. Чтение/запись и фасадные функции
// возвращают NotImplemented и будут реализованы на этапах 2 и 5.
